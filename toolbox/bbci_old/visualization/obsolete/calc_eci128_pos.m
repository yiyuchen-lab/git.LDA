function [x,y,z,Clab]= calc_eci128_pos;
%[x,y,z,clab]= calc_eci_128_pos;
% or
%posSystem= calc_eci_128_pos;

bbci_obsolete(mfilename, 'use calc_pos_eci128');

Clab= {'Fpz', ...
       'Fp1','Fp1p','Fp2p','Fp2', ...
       'F7a','F3a','Fza','F4a','F8a', ...
       'F5a','F1a','F2a','F6a', ...
       'F7','F5','F3','F1','Fz','F2','F4','F6','F8', ...
       'TF5a','F5a1','F3a1','F1a1','F2a1','F4a1','F6a1','TF6a', ...
       'T1','T3a','F5p','F3p','F1p','Fzp','F2p','F4p','F6p','T4a','T2', ...
       'C7a','C5a','C3a','C1a','C2a','C4a','C6a','C8a', ...
       'T3l','T3','C5','C3','C1','Cz','C2','C4','C6','T4','T4l', ...
       'C7p','C5p','C3p','C1p','C2p','C4p','C6p','C8p', ...
       'T5al','T5a','P5a','P3a','P1a','Pza','P2a','P4a','P6a','T6a','T6al', ...
       'TP5a','P35a','P31a','P1za','Pz2a','P24a','P46a','PT6a', ...
       'T5l','T5','P5','P3','P1','Pz','P2','P4','P6','T6','T6l', ...
       'P5p','P1p','P2p','P6p', ...
       'T71l','T7','P3p','Pzp','P4p','T8','T8l1', ...
       'T7l','O1','O1a','O2a','O2','T8l' ...
       'Oz', ...
       'O1zp','Oz2p', ...
       'O1p','Ozp','O2p'};

a= 90/4;
b= 90/4;

ea= a * [0 ...
         -4 -2 2 4 ...
         -4 -2 0 2 4 ...
         -3 -1 1 3 ...
         -4:4 ...
         -3.5:3.5 ...
         -5:5 ...
         -3.5:3.5 ...
         -5:5 ...
         -3.5:3.5 ...
         -5:5 ...
         -3.5:3.5 ...
         -5:5 ...
         -2.5 -1 1 2.5 ...
         -4 -3 -2 0 2 3 4 ...
         -5 -3.5 -1 1 3.5 5 ...
         0 ...
         -2 2 ...
         -4 0 4 ];

eb= [   4*b * ones(1,1) ...
      3.5*b * ones(1,4) ...
        3*b * ones(1,5) ...
      2.5*b * ones(1,4) ...
        2*b * ones(1,9) ...
      1.5*b * ones(1,8) ...
        1*b * ones(1,11) ...
      0.5*b * ones(1,8) ...
        0*b * ones(1,11) ...
     -0.5*b * ones(1,8) ...
       -1*b * ones(1,11) ...
     -1.5*b * ones(1,8) ...
       -2*b * ones(1,11) ...
     -2.5*b * ones(1,4) ...
       -3*b * ones(1,7) ...
     -3.5*b * ones(1,6) ...
       -4*b * ones(1,1) ...
     -4.5*b * ones(1,2) ...
       -5*b * ones(1,3) ];

[x,y,z]= abr2xyz(ea, eb);

if nargout==1,
  posSystem.x= x;
  posSystem.y= y;
  posSystem.z= z;
  posSystem.clab= clab;
  x= posSystem;
end
